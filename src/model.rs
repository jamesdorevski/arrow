use chrono::{DateTime, Local};

pub struct Project {
    pub id: u32,
    pub name: String,
    pub description: Option<String>,
    pub created: DateTime<Local>,
    pub updated: DateTime<Local>,
}

impl Project {
    pub fn new(
        id: u32,
        name: String,
        description: Option<String>,
        created: DateTime<Local>,
        updated: DateTime<Local>,
    ) -> Self {
        Project {
            id, // id is generated by db
            name,
            description,
            created,
            updated,
        }
    }
}

pub struct Log {
    pub id: u32,
    pub proj_id: u32,
    pub message: String,
    pub start: DateTime<Local>,
    pub end: DateTime<Local>,
}

impl Log {
    pub fn new(
        id: u32,
        proj_id: u32,
        message: String,
        start: DateTime<Local>,
        end: DateTime<Local>,
    ) -> Self {
        Log {
            id, // db-generated
            proj_id,
            message,
            start,
            end,
        }
    }
}
